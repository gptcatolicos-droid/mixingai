// Detectar idioma por IP/browser
export const detectLang = async (): Promise<Lang> => {
  // 1. Preferencia guardada
  const saved = localStorage.getItem('lang') as Lang;
  if (saved === 'en' || saved === 'es') return saved;
  // 2. Intentar por IP (ipapi.co es gratuito)
  try {
    const r = await fetch('https://ipapi.co/json/', { signal: AbortSignal.timeout(2000) });
    const d = await r.json();
    const spanishCountries = ['CO','MX','ES','AR','PE','CL','VE','EC','GT','CU','BO','DO','HN','PY','SV','NI','CR','PA','UY','GQ','PR'];
    return spanishCountries.includes(d.country_code) ? 'es' : 'en';
  } catch {
    // 3. Fallback por browser
    return navigator.language?.toLowerCase().startsWith('es') ? 'es' : 'en';
  }
};
